def jedec_mfg_name_from_bytes(mfg_ids):
    bank = 0
    for mfg_id in mfg_ids:
        if mfg_id == 0x7F:
            bank += 1
        else:
            for (number, name, code) in _manufacturers[bank]:
                if code == mfg_id:
                    return name


def jedec_mfg_name_from_bank_num(bank, mfg_num):
    for (number, name, code) in _manufacturers[bank]:
        if number == mfg_num:
            return name


